namespace BZ2TerrainEditor
{
	/// <summary>
	/// Represents a RGB color triplet.
	/// </summary>
	public struct RGB
	{
		#region Fields

		public byte R;
		public byte G;
		public byte B;

		#endregion

		#region Properties

		#endregion

		#region Constructors

		/// <summary>
		/// Creates a new RGB.
		/// </summary>
		public RGB(byte r, byte g, byte b)
		{
			this.R = r;
			this.G = g;
			this.B = b;
		}

		#endregion

		#region Methods

		#endregion
	}
}
